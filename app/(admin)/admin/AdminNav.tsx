export default function AdminNav() {
  const pathname = usePathname();
  const router = useRouter();

  async function handleLogout() {
    const { createClient } = await import("@/lib/supabase/client");
    const supabase = createClient();
    await supabase.auth.signOut();
    router.push("/");
    router.refresh();
  }

  return (
    <aside className="w-56 bg-white border-r border-gray-200 min-h-screen flex flex-col shrink-0">
      <div className="p-4 border-b border-gray-200">
        <h1 className="text-sm font-bold text-[#1F4E79]">Admin</h1>
        <p className="text-xs text-gray-400">Utah Hospitality Insiders</p>
      </div>

      <nav className="flex-1 py-3">
        {NAV_ITEMS.map((item) => {
          const isActive = item.href === "/admin"
            ? pathname === "/admin"
            : item.href === "/admin/jobs"
            ? pathname === "/admin/jobs"
            : pathname.startsWith(item.href);

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-2.5 px-4 py-2 text-sm transition-colors ${
                isActive
                  ? "bg-blue-50 text-[#1F4E79] font-medium border-r-2 border-[#1F4E79]"
                  : "text-gray-600 hover:bg-gray-50"
              }`}
            >
              <svg className="w-4 h-4 shrink-0" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d={item.icon} />
              </svg>
              {item.label}
            </Link>
          );
        })}
      </nav>

      <div className="p-4 border-t border-gray-200">
        <button
          onClick={handleLogout}
          className="text-xs text-gray-500 hover:text-red-600 font-medium"
        >
          Log Out
        </button>
      </div>
    </aside>
  );
}
